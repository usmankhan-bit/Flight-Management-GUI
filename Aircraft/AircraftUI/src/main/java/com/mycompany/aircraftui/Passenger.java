    private void displayPassengerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_displayPassengerMouseClicked
        // TODO add your handling code here:
        ac.display();
        jTable1.removeAll();
        dtm.setRowCount(0);
        String header[] = new String[] { "Ticket Id", "Seat No", "Passenger Name",
        "Flight No", "Fly Points"};
        dtm.setColumnIdentifiers(header);
        jTable1.setModel(dtm);
        ArrayList <Ticket> temp=ac.returnfun();
        for(Ticket t: temp)
        {
            Vector<Object> data = new Vector<Object>();
            System.out.println(t.tid+"printing");
            data.add(t.tid);
            data.add(t.seatNum);
            data.add(t.p.passName);
            data.add(t.p.flightnum);
            data.add(t.p.flyPoints);
            dtm.addRow(data);
        }
    }//GEN-LAST:event_displayPassengerMouseClicked

