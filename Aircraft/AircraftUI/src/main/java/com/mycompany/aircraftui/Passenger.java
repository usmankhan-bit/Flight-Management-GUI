    private void submit2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit2MouseClicked
        // TODO add your handling code here:
        if(AddPassenger.isSelected())
        {
            String passname=passName.getText();
            int fp=Integer.parseInt(points.getText());
            int inp=JOptionPane.showConfirmDialog(this, "Do you want to proceed","Confirm",JOptionPane.YES_NO_OPTION);
            if(inp==0)
            {
                try{
                    ac.add(passname, fp);
                    System.out.println("Passenger Added");
                    JOptionPane.showMessageDialog(this,"Passenger Added","Success",JOptionPane.INFORMATION_MESSAGE);
                }
                catch(MyException e)
                {
                    e.print();
                    JOptionPane.showMessageDialog(this,"Sorry Seats Full","Fail",JOptionPane.ERROR_MESSAGE);
                }
                
                
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Booking Aborted","Fail",JOptionPane.ERROR_MESSAGE);
            }
                
            
        }
        else if(RemovePassenger.isSelected())
        {
            int ticId=Integer.parseInt(tid.getText());
            int inp=JOptionPane.showConfirmDialog(this, "Do you want to proceed","Confirm",JOptionPane.YES_NO_OPTION);
            if(inp==0)
            {
                try{
                    ac.delete(ticId);
                    System.out.println("Passenger Deleted");
                    JOptionPane.showMessageDialog(this,"Passenger Deleted","Success",JOptionPane.INFORMATION_MESSAGE);
                }
                catch(MyException e)
                {
                    e.print();
                    JOptionPane.showMessageDialog(this,"Ticket ID not found","Fail",JOptionPane.ERROR_MESSAGE);
                }
                
                
            }else
            {
                JOptionPane.showMessageDialog(this,"Deletion Failed","Fail",JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
        else if(printTicket.isSelected())
        {
            int ticId=Integer.parseInt(tid.getText());
            ArrayList <Ticket> temp=ac.returnfun();
            int flag=0;
            for(Ticket t:temp)
            {
                if(t.tid==ticId)
                {
                    System.out.println("TIcket found");
                    flag=1;
                    String fname=ticId+".txt";
                    try
                    {
                       FileWriter fw=new FileWriter(fname);
                       fw.write("Welcome Onboard, Happy to Serve you\n");
                       fw.write("=============================\n");
                       fw.write("Name : "+t.p.passName+"\t"+"Flight No: "+ac.flightnum+"\n");
                       fw.write("FROM : "+ac.departure+"~-~-~-~-~-~-~-~-~"+"TO : "+ac.arrival+"\n");
                       fw.write("Seat No: "+t.seatNum+"\n");
                       fw.write("Please make sure you start boarding 45 minutes prior to the departure time!");
                       fw.close();
                    }catch(Exception e){System.out.println(e);}  
                    System.out.println("Write successful");
                    JOptionPane.showMessageDialog(this,"Ticket Generated","Success",JOptionPane.INFORMATION_MESSAGE);
                       
                }
            }
            if(flag==0)
                JOptionPane.showMessageDialog(this,"Ticket not Found","Fail",JOptionPane.ERROR_MESSAGE);
        
        }
        
        
    }//GEN-LAST:event_submit2MouseClicked

